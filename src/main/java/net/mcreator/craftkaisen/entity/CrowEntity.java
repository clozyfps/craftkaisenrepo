	@Override
	protected void registerGoals() {
		super.registerGoals();
		this.goalSelector.addGoal(1, new OwnerHurtByTargetGoal(this));
		this.targetSelector.addGoal(2, new OwnerHurtTargetGoal(this));
		this.goalSelector.addGoal(3, new FollowOwnerGoal(this, 1, (float) 10, (float) 2, false));
		this.goalSelector.addGoal(4, new Goal() {
			{
				this.setFlags(EnumSet.of(Goal.Flag.MOVE));
			}

			public boolean canUse() {
				if (CrowEntity.this.getTarget() != null && !CrowEntity.this.getMoveControl().hasWanted()) {
					return true;
				} else {
					return false;
				}
			}

			@Override
			public boolean canContinueToUse() {
				return CrowEntity.this.getMoveControl().hasWanted() && CrowEntity.this.getTarget() != null && CrowEntity.this.getTarget().isAlive();
			}

			@Override
			public void start() {
				LivingEntity livingentity = CrowEntity.this.getTarget();
				Vec3 vec3d = livingentity.getEyePosition(1);
				CrowEntity.this.moveControl.setWantedPosition(vec3d.x, vec3d.y, vec3d.z, 3);
			}

			@Override
			public void tick() {
				LivingEntity livingentity = CrowEntity.this.getTarget();
				if (CrowEntity.this.getBoundingBox().intersects(livingentity.getBoundingBox())) {
					CrowEntity.this.doHurtTarget(livingentity);
				} else {
					double d0 = CrowEntity.this.distanceToSqr(livingentity);
					if (d0 < 24) {
						Vec3 vec3d = livingentity.getEyePosition(1);
						CrowEntity.this.moveControl.setWantedPosition(vec3d.x, vec3d.y, vec3d.z, 3);
					}
				}
			}
		});
		this.goalSelector.addGoal(5, new RandomStrollGoal(this, 2.6, 20) {
			@Override
			protected Vec3 getPosition() {
				RandomSource random = CrowEntity.this.getRandom();
				double dir_x = CrowEntity.this.getX() + ((random.nextFloat() * 2 - 1) * 16);
				double dir_y = CrowEntity.this.getY() + ((random.nextFloat() * 2 - 1) * 16);
				double dir_z = CrowEntity.this.getZ() + ((random.nextFloat() * 2 - 1) * 16);
				return new Vec3(dir_x, dir_y, dir_z);
			}
		});
		this.targetSelector.addGoal(6, new HurtByTargetGoal(this).setAlertOthers());
		this.goalSelector.addGoal(7, new RandomLookAroundGoal(this));
		this.goalSelector.addGoal(8, new FloatGoal(this));
	}

